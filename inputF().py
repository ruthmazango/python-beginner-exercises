#____________________________Area of a circle________________________________

radius = float(input('Enter the radius of your circle:> '))

pi = 3.14159

area_of_circle = pi * radius ** 2

print(f'+++++++++++++++++++++\n Area of circle with radius: {radius}, is {area_of_circle}')